package DTO;

/**
 *
 * @author Nick
 */
public class AdditionImageDTO {
    private String image;
    private String caption;

    public AdditionImageDTO() {
    }

    public AdditionImageDTO(String image, String caption) {
        this.image = image;
        this.caption = caption;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getCaption() {
        return caption;
    }

    public void setCaption(String caption) {
        this.caption = caption;
    }     
}
